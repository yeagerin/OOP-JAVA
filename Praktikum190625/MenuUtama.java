    private void btnHitungKembalianActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHitungKembalianActionPerformed
        Double bayar = Double.parseDouble(txtTotalBayar.getText());
        Double uang = Double.parseDouble(txtUang.getText());
        if(uang<bayar){
            JOptionPane.showMessageDialog(this, "Uang Anda Kurang", "Peringatan", JOptionPane.WARNING_MESSAGE);
            txtUang.requestFocus();
        } else{
            Double kembalian = uang - bayar;
            txtKembalian.setText(Double.toString(kembalian));
        }
    }//GEN-LAST:event_btnHitungKembalianActionPerformed

    private void btnKeluarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKeluarActionPerformed
        btnKeluar.addActionListener(new java.awt.event.ActionListener() {
            public void actionPerformed(java.awt.event.ActionEvent evt) {
                int confirm = JOptionPane.showConfirmDialog(
                    null, 
                    "Apakah Anda yakin ingin keluar?", 
                    "Konfirmasi Keluar", 
                    JOptionPane.YES_NO_OPTION
                );
                if (confirm == JOptionPane.YES_OPTION) {
                    System.exit(0);
                }
            }
        });
    }//GEN-LAST:event_btnKeluarActionPerformed

