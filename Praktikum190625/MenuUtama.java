    private void btnTotalActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTotalActionPerformed
        if (txtNamaPelanggan.getText().isEmpty() || txtNoMeja.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Nama Pelanggan dan No Meja harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    Double makanan = Double.parseDouble(txtTotalMakanan.getText());
    Double minuman = Double.parseDouble(txtTotalMinuman.getText());
    Double total = makanan + minuman;
    Double pajak = 0.1 * total;
    Double bayar = total + pajak;
    txtTotalBayar.setText(Double.toString(bayar));
    txtPajak.setText(Double.toString(pajak));
    txtTotalHarga.setText(Double.toString(total));
        
    }//GEN-LAST:event_btnTotalActionPerformed

